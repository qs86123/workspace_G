package com.changhong.data.jxw.ktr.entity;

public class DataPackageColumn
{
    private String id;
    private String dptId;
    private String columnSource;
    private String columnName;
    private String columnType;

    public String getId()
    {
        return id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getDitId()
    {
        return dptId;
    }

    public void setDitId(String dptId)
    {
        this.dptId = dptId;
    }

    public String getColumnSource()
    {
        return columnSource;
    }

    public void setColumnSource(String columnSource)
    {
        this.columnSource = columnSource;
    }

    public String getColumnName()
    {
        return columnName;
    }

    public void setColumnName(String columnName)
    {
        this.columnName = columnName;
    }

    public String getColumnType()
    {
        return columnType;
    }

    public void setColumnType(String columnType)
    {
        this.columnType = columnType;
    }
}
